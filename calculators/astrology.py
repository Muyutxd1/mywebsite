    def _detect_patterns(self, lines, planet_houses, planet_signs, sign_idx, aspects):
        """Detect chart patterns: stellium, grand trine, T-square."""
        # Stellium: 3+ planets in same sign or same house
        from collections import Counter
        house_counts = Counter(planet_houses.values())
        sign_counts = Counter(planet_signs.values())

        for h, n in house_counts.most_common(2):
            if n >= 3:
                planets_in = [p for p, ph in planet_houses.items() if ph == h]
                lines.append(f'▸ 第{h}宫星群（{n}颗行星：{"、".join(planets_in)}）——'
                           f'此宫位所代表的生活领域是你人生的绝对重心，能量高度集中。')

        for s, n in sign_counts.most_common(2):
            if n >= 3:
                planets_in = [p for p, ps in planet_signs.items() if ps == s]
                lines.append(f'▸ {s}座星群（{n}颗行星：{"、".join(planets_in)}）——'
                           f'{s}座的能量贯穿你的人生，其特质在多个层面被强调。')

        # Grand Trine: 3 planets in same element, 120° apart
        elements = {'火': (0,4,8), '土': (1,5,9), '风': (2,6,10), '水': (3,7,11)}
        el_planets = {el: [] for el in elements}
        for p, s in planet_signs.items():
            si = sign_idx.get(s, -1)
            for el, indices in elements.items():
                if si in indices:
                    el_planets[el].append(p)
        for el, plist in el_planets.items():
            if len(plist) >= 3:
                lines.append(f'▸ 大三角（{el}象）：{"、".join(plist)}同属{el}象——'
                           f'{el}的能量在三个维度上流畅运转，是你最天然的天赋所在。')
                lines.append(f'   这种配置让你在{el}象相关领域事半功倍，但需注意不要因过于舒适而缺乏挑战。')

        # T-Square: 2 planets opposition, both square to a 3rd
        # Simplified: check for aspects
        opp_pairs = []
        sq_planets = {}
        for a in (aspects or []):
            if a['aspect'] == '冲':
                opp_pairs.append((a['p1'], a['p2']))
            if a['aspect'] == '刑':
                sq_planets.setdefault(a['p1'], []).append(a['p2'])
                sq_planets.setdefault(a['p2'], []).append(a['p1'])

        for p1, p2 in opp_pairs:
            for p3 in sq_planets.get(p1, []):
                if p3 in sq_planets.get(p2, []):
                    lines.append(f'▸ T三角格局：{p1}冲{p2}，两者同时刑{p3}——')
                    lines.append(f'   这是内在张力的集中体现。{p3}所在领域是化解这组矛盾的关键出口。')
                    lines.append(f'   学会将张力转化为行动力，这是你的成长课题也是巨大的潜力所在。')
                    break

        if not any(n >= 3 for n in list(house_counts.values()) + list(sign_counts.values())):
            if not any(len(plist) >= 3 for plist in el_planets.values()):
                lines.append('（无特别显著的星群或大三角格局——行星能量分布较均衡，人生较为多元。）')
